# Usar un ciclo for para imprimir los números del 1 al 10

for numero in range (1, 11):
    print(numero)

# Inicializar la variable de control
numero = 1

# Usar un ciclo while para imprimir los números del 1 al 5
while numero <= 5:
    print(numero)
    numero += 1  # Incrementar la variable de control